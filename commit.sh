#!/bin/bash

# Отримуємо повідомлення коміту від користувача або використовуємо стандартне
MESSAGE=${1:-"Update project"}

echo "🚀 Починаємо процес коміту..."

# Додаємо всі зміни
git add .

# Перевіряємо чи є зміни для коміту
if git diff --cached --quiet; then
    echo "✅ Немає нових змін для коміту."
else
    # Комітимо зміни
    git commit -m "$MESSAGE"
    echo "📦 Зміни закомічено: $MESSAGE"
fi

# Пушимо в репозиторій
echo "⬆️ Надсилаємо зміни на GitHub..."
git push

if [ $? -eq 0 ]; then
    echo "🎉 Успішно оновлено!"
else
    echo "❌ Помилка під час надсилання (push). Перевірте підключення або конфлікти."
fi
